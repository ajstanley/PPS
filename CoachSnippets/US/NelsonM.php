    <p>Matt started his kayaking career working as a kayak guide in Alaska and Washington, and eventually landed on
      Orcas Island where he started working with Body Boat Blade in 2005.
    </p>
    <p>
      In 2008, Matt was one of a three-person team to circumnavigate the Kenai Penninsula, Alaska. This was a 500 mile
      journey with which they also raised funds for an environmental non-profit (ourowntwohands.blogspot.com).
    </p>
    <p>
      Matt is a current BCU coach and holds the 5 star award for Sea Kayak, is a Coach level 3 Sea, holds the 3
      star Canoe award, and is a P&H team paddler. He works at paddle sport events in the U.S. and internationally.
      In Mattʼs words, “I get a lot of satisfaction out of sharing my passion and experience of the sea while making
      it possible for other folks to get out there as well.”
     
