    <p>Dubside is known internationally as an authority on Greenland rolling and rope gymnastics. He has appeared in Justine Curgenven’s
      "This is the Sea Four", Bryan Smith’s "Pacific Horizons", and magazine articles in "Sea Kayaker", "Canoe and Kayak", "Paddler",
