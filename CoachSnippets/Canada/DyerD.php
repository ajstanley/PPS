  <div class="coach_body">
    <p>Tim Dyer and his wife Kathy are the founders of White Squall Paddling Centre in Ontario.  White Squall provides a
      wide range sea kayak training, certification and trips on Georgian Bay heading into its 28th season. Tim’s initial
      introduction to west coast paddling was by signing on at Strathcona Outdoor Centre, guiding and teaching on the
      west coast of Vancouver Island for two seasons. He remained in BC for several years, teaching elementary & high
      school in Terrace, Kitwancool and Pemberton.
    </p>
    <p>Tim helped with the initial establishment of the sea kayak programme in Canada, serving on the PDC for many years. 
      He’s a PC Instructor Trainer Level 3 who strongly advocates for mentoring as an on-going way to develop the skills, 
      knowledge and judgement needed for instructing and leading.  For many years Tim and Squall staff sponsored the 
      Georgian Bay Sea Kayak Symposium and he now co-ordinates the Georgian Bay Storm Gathering with David Johnston. 
      He is very excited to be at Pearson College and a weekend away in some of the best paddling waters on the planet.
    </p>
