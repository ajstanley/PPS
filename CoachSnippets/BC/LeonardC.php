      Costain taught canoeing and kayaking in the freshwaters of Ontario for a decade. He flirted with paddling on the west coast for a couple of
      years and eventually relocated to BC. 
    </p>

    <p>
      A multifaceted teacher, Costain’s instructional experience ranges from recreational youth and adult courses to instructor and leadership programs.
      He seeks out unique paddling environments and loves to challenge what has traditionally been done in sea kayaks.  Costain has sought instruction
      and inspiration from many paddling communities. Costain is a SKGABC Level 3 Guide, Paddle Canada Sea Kayak Level 4 Paddler, Level 2 Instructor,
      Level 1 Instructor Trainer, and Intermediate Tandem Canoe Instructor.
