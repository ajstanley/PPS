  <div class="coach_body">
    <p>Jordie became interested in the outdoors as a child through the scouting movement and his nature loving parents.
      It lead him to venture out on his own with friends to explore rivers and forests in his home community of
      Courtenay. Paddling became a peaceful way for him to relate to nature, to help people achieve their goals, to
      practice leadership and to engage in life affirming risk taking.
    </p>
    <p> He enjoys long distance adventure kayaking and has crossed Dixon Entrance East, Georgia Strait,
      Juan de Fuca Strait and Queen Charelotte Strait from Cape Caution to God's Pocket. He has worked as a Sea Kayak
      Instructor and Guide with Ocean River Sports for 17 years, and is a registered with the SKGABC, Paddle Canada,
      BCU, Slipstreams First Aid and White Water Instructing Association of BC.</p>
